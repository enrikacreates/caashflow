'use server'

import { createClient } from '@/lib/supabase/server'
import { getUserHouseholdId, getAuthUser } from '@/lib/supabase/helpers'
import { DEFAULT_BASE_BUDGET, DEFAULT_REQUESTS } from '@/lib/constants'
import { revalidatePath } from 'next/cache'

// ── Member Management ──────────────────────────────────────────────────────

export async function getHouseholdMembers() {
  const supabase = await createClient()
  const householdId = await getUserHouseholdId()

  const { data, error } = await supabase
    .from('household_members')
    .select('*')
    .eq('household_id', householdId)
    .order('created_at', { ascending: true })

  if (error) throw new Error(`Failed to fetch members: ${error.message}`)
  return data
}

export async function generateInviteLink(): Promise<string> {
  const supabase = await createClient()
  const householdId = await getUserHouseholdId()
  const user = await getAuthUser()

  // Expire any existing pending invites for this household first
  await supabase
    .from('household_invites')
    .update({ status: 'expired' })
    .eq('household_id', householdId)
    .eq('status', 'pending')

  // Create a fresh invite (token generated by DB default)
  const { data, error } = await supabase
    .from('household_invites')
    .insert({
      household_id: householdId,
      invited_by: user?.id,
    })
    .select('token')
    .single()

  if (error) throw new Error(`Failed to create invite: ${error.message}`)
  return data.token
}

export async function removeHouseholdMember(memberId: string) {
  const supabase = await createClient()
  const householdId = await getUserHouseholdId()
  const user = await getAuthUser()

  // Can't remove yourself
  const { data: self } = await supabase
    .from('household_members')
    .select('id')
    .eq('household_id', householdId)
    .eq('user_id', user?.id)
    .single()

  if (self?.id === memberId) throw new Error('You cannot remove yourself')

  const { error } = await supabase
    .from('household_members')
    .delete()
    .eq('id', memberId)
    .eq('household_id', householdId)

  if (error) throw new Error(`Failed to remove member: ${error.message}`)
  revalidatePath('/settings')
}

// ── Invite Validation (public — used by join page) ─────────────────────────

export async function getInviteDetails(token: string) {
  const supabase = await createClient()

  const { data, error } = await supabase
    .from('household_invites')
    .select('id, status, expires_at, household_id, households(name)')
    .eq('token', token)
    .single()

  if (error || !data) return null
  if (data.status !== 'pending') return null
  if (new Date(data.expires_at) < new Date()) return null

  return {
    householdName: (data.households as { name: string } | null)?.name ?? 'Your household',
    token,
  }
}

